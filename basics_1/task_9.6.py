# Задача 2. Марсоход-2
# К роботу Валли планируют отправить «коллегу» Билли. Это первая высадка Билли на Марс,
# поэтому его тестируют в прямоугольном помещении размером 15 × 20 м. Марсоход высаживается
# в центре комнаты (в точке 8, 10), затем управление им передаётся оператору, то есть пользователю
# вашей программы.
# Программа спрашивает, в какую сторону оператор хочет направить робота: север (клавиша W), юг
# (клавиша S), запад (клавиша A) или восток (клавиша D). Оператор делает выбор, марсоход перемещается
# в эту сторону на один метр, а программа сообщает новую позицию робота. Если марсоход упёрся в стену,
# он не должен пытаться переместиться в сторону стены — в этом случае его позиция не меняется.
