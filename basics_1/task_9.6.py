# Обращайте внимание на границы.
# Попробуйте изменять положение марсохода только после проверки координат.
# Ввод команд происходит только в верхнем регистре, учитывать нижний не нужно.
# Старайтесь максимально уменьшать количество дублирований кода. Если действие выполняется независимо от
# условий, не стоит дублировать его в каждом условии.
# Что оценивается.
# Результат вывода соответствует условию.
# Формат вывода соответствует примеру.
# Марсоход не двигается, если достигает границы участка.

X = 8
Y = 10
v_limit = 20
h_limit = 15
count_x = X
count_y = Y

while True:
        enter_position = input(f"Марсоход находится на позиции {count_x, count_y}, введите команду: ")

        if enter_position == "W" and count_y < v_limit:
            count_y += 1
        elif enter_position == "D" and count_x < h_limit:
            count_x += 1
        elif enter_position == "S" and count_y > 0:
            count_y -= 1
        elif enter_position == "A" and count_x > 0:
            count_x -= 1
        elif enter_position == "stop":
            print(f"Марсоход находится на позиции {count_x, count_y}.")
            break
        else:
            print("Вы ввели неправильную команду! Повторите запрос.")
